// Condicionais: Switch

// O switch é uma estrutura de controle que permite a execução de diferentes blocos de código, dependendo do valor de uma variável.
// A estrutura do switch é composta por um bloco de código que contém um ou mais casos.

let cor = "cmnmssmkc";

switch (cor) {
  case "verde":
    console.log("siga");
    break;
  case "amarelo":
    console.log("atenção");
    break;
  case "vermelho":
    console.log("pare");
    break;
  default:
    console.log("Desconhecido");
}
