// Operadpres aritméticos básicos

console.log(2 + 2); // 4 -> adição
console.log(2 - 2); // 0 -> subtração
console.log(2 * 2); // 4 -> multiplicação
console.log(2 / 2); // 1 -> divisão
console.log(2 % 1); // 0 -> resto da divisão
console.log(2 ** 2); // 4 -> 2 elevado a 2
console.log(2 ** 3); // 8 -> 2 elevado a 3