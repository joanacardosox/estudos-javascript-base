//Operadores de atribuição -> atribuem valores a variáveis

let x = 10;

x += 5; // x = x + 5
console.log(x); // 15

x -= 5; // x = x - 5
console.log(x); // 10

x *= 5; // x = x * 5
console.log(x); // 50

x /= 5; // x = x / 5
console.log(x); // 10

x %= 5; // x = x % 5
console.log(x); // 0

x **= 5; // x = x ** 5
console.log(x); // 0

